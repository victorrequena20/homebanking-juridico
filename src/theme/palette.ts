function createGradient(color1: string, color2: string) {
  return `linear-gradient(to bottom, ${color1}, ${color1}, ${color2})`;
}

function createGradientRight(color1: string, color2: string) {
  return `linear-gradient(to right, ${color1}, ${color2})`;
}

export type ColorSchema =
  | "primary"
  | "secondary"
  | "info"
  | "success"
  | "warning"
  | "error";

interface GradientsPaletteOptions {
  primary: string;
  secondary: string;
  secondaryRight: string;
  info: string;
  success: string;
  warning: string;
  error: string;
}

interface ChartPaletteOptions {
  violet: string[];
  blue: string[];
  green: string[];
  yellow: string[];
  red: string[];
}

